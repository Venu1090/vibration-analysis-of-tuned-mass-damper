s = 1;
%.................... zeta = 1
% .....................mue = 0.05
zeta = 1;
mue = 0.05;
g = 0.5:0.001:1.5;

tzg2 = (2*zeta.*g).^2 ; 
%.......................tzg2 = (2*zeta*g)^2
g2_s2_2 = (g.^2-s.^2).^2 ;
%........................g2_s2-2 = (g.^2-s.^2)^2
g2_1mueg2_2 = (g.^2-1+mue.*g.^2).^2;
mues2g2 = mue.*s.^2*g.^2;
g2_1 = g.^2-1 ;
g2_s2 = g.^2-s.^2;
x1_r =sqrt((tzg2+g2_s2_2)./(tzg2.*g2_1mueg2_2+(mues2g2-g2_1.*g2_s2).^2));
x2_r =sqrt((tzg2+s.^4)./(tzg2.*g2_1mueg2_2+(mues2g2-g2_1.*g2_s2).^2));
plot(g,x1_r,'--',g,x2_r,'-o');
hold on
